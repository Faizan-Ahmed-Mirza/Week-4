void gotoxy(int x, int y)
{

  COORD coordinates;
  coordinates.X = x;
  coordinates.Y = y;
  SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), coordinates);

}

void playerMove(int x, int y)
{

while(y < 27){
gotoxy(x,y);
cout << " ";
y = y + 1;
gotoxy(x,y);
cout << "P";
gotoxy(x,y);
Sleep(100);

}

while(y>1){
gotoxy(x,y--);
cout << " ";
gotoxy(x,y);
cout << "P";
Sleep(100);
}
}

